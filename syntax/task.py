# Calculate the area and perimeter of a rectangle.

# 1. Prompt the user to enter the length and width of a rectangle.
# 2. Store the length in a variable called length and the width in a variable called width.
# 3. Calculate the area of the rectangle by multiplying the length and width and store it in a variable called area.
# 4. Calculate the perimeter of the rectangle by adding twice the length and twice the width, and store it in a variable called perimeter.
# 5. Print the calculated area and perimeter with appropriate labels.

length = float(input("Enter the length of the rectangle: "))
width = float(input("Enter the width of the rectangle: "))

area = length * width
perimeter = 2 * (length + width)

print("The area of the rectangle is:", area)
print("The perimeter of the rectangle is:", perimeter)

